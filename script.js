var daysUntilMyBirthday = 60;

while (daysUntilMyBirthday > 30) {
    console.log (daysUntilMyBirthday + " days until my birthday, I wish my birthday was closer");
    daysUntilMyBirthday --;
} 
while (daysUntilMyBirthday > 5) {
    console.log (daysUntilMyBirthday + " days until my birthday, it's almost my birthday!");
    daysUntilMyBirthday --;
} 
while (daysUntilMyBirthday > 0) {
    console.log (daysUntilMyBirthday + " days until my birthday, HEEELLL YYEEEAAA!");
    daysUntilMyBirthday --;
}
while (daysUntilMyBirthday == 0) {
    console.log ("HAPPY BIRTHDAY!");
    daysUntilMyBirthday --;
}